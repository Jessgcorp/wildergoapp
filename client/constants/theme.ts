/**
 * Add opacity to any WilderGo brand color
 * @example withOpacity('burntSienna', 0.1) // 10% opacity
 */
export const withOpacity = (colorKey: string, opacity: number): string => {
  const rgbValue = RGB[colorKey];
  if (!rgbValue) {
    console.warn(`Color "${colorKey}" not found in RGB. Using black.`);
    return `rgba(0, 0, 0, ${opacity})`;
  }
  return `rgba(${rgbValue}, ${opacity})`;
};

/**
 * Get mode color by mode name
 * @example getModeColor('dating') // Returns '#D94848'
 */
export const getModeColor = (mode: string): string => {
  const modeMap: Record<string, string> = {
    dating: colors.modes.dating,
    friend: colors.modes.friends,
    friends: colors.modes.friends,
    builder: colors.modes.builder,
    builders: colors.modes.builder,
    emergency: colors.modes.help,
    help: colors.modes.help,
  };
  return modeMap[mode?.toLowerCase()] || colors.burntSienna[500];
};

/**
 * Get mode accent object by mode name
 * @example getModeAccent('dating') // Returns full accent config
 */
export const getModeAccent = (mode: string) => {
  const modeKey = mode?.toLowerCase();
  if (modeKey === "dating") return colors.modeAccent.dating;
  if (modeKey === "friend" || modeKey === "friends")
    return colors.modeAccent.friends;
  if (modeKey === "builder" || modeKey === "builders")
    return colors.modeAccent.builder;
  if (modeKey === "help" || modeKey === "emergency")
    return colors.modeAccent.help;
  return colors.modeAccent.dating;
};

